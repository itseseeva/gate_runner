/// <summary>
/// Статус-эффекты на враге.
/// Каждый эффект имеет длительность и обновляется при новом ударе.
/// </summary>
public enum StatusEffectType
{
    None,
    Burning,   // тикает уроном раз в секунду
    Frozen,    // замедляет движение врага вдвое
    Shocked,   // враг получает +40% урона от любых источников
}
